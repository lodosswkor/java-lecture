
// 단항연산자(증감연산) 
public class OpExample {
	
	public static void main(String[] args) {
	    
		// ++ 
		int b = 10;
		System.out.println(b++);
		System.out.println(b);
		
		int c = 10; 
		System.out.println(++c);
		System.out.println(c);
		
		// -- d-- => d = d - 1;
		int d = 10; 
		System.out.println(d --);
		System.out.println(d);
		
		int e = 10; 
		System.out.println(-- e);
		System.out.println(e);
		
		
		
		
		
	}
	
}
